    private void MoveItemButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MoveItemButtonActionPerformed

        //Check the file type selected in the tree
        String TypeCheck = "";
        if(Global.DirectoryRootAddress != "" && Global.ItemSelectedFlagTree1 == 1)
        {
            TypeCheck = getFileExtension.getFileExtension(jTree1.getLeadSelectionPath().getLastPathComponent().toString());
        }

        //Copy the file to the selected project
        if(Global.ItemSelectedFlagTree1 == 1 && Global.ItemSelectedFlagTree2 == 1 && TypeCheck.equals("txt"))
        {

            File dirFrom = new File(Global.SelectedJtree1ItemAdress);
            File dirTo = new File(Global.DirectoryOfProjectsAddress + "\\"+ jTree2.getLeadSelectionPath().getLastPathComponent().toString()+ "\\"+ jTree1.getLeadSelectionPath().getLastPathComponent().toString());

                try {
                    //copyFile is a method in copyFile.class
                    CopyFile.copyFile(dirFrom, dirTo);
                    System.out.println("File moved successfully");
                    ChangeProjectTreeAddress();
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(null, "Error!");
                }

            }
            else{

                if(Global.ItemSelectedFlagTree1 == 0)
                JOptionPane.showMessageDialog(null, "Please insert a Directory And Choose .txt file!");
                else
                if(!TypeCheck.equals("txt"))
                JOptionPane.showMessageDialog(null, "Only a file with .txt type must be selected");
                else
                if(Global.ItemSelectedFlagTree2 == 0)
                JOptionPane.showMessageDialog(null, "Please select a project file!");

            }
    }//GEN-LAST:event_MoveItemButtonActionPerformed

