    private void jTree2MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTree2MousePressed
        int selRow = jTree2.getRowForLocation(evt.getX(), evt.getY());
        TreePath selPath = jTree2.getPathForLocation(evt.getX(), evt.getY());
        if(selRow != -1) {
            if(evt.getClickCount() == 1) {
                Global.ItemSelectedFlagTree2 = 1;
            }
            else if(evt.getClickCount() == 2) {
                //Open Tab
                path = Global.DirectoryOfProjectsAddress + "\\" +
                jTree2.getLeadSelectionPath().getPathComponent(WIDTH).toString() + "\\" +
                jTree2.getLeadSelectionPath().getLastPathComponent().toString();
                //Call the class to add tab
                DirCh = new File(this.path);
                if(DirCh.exists())
                {
                    System.out.println(path);

                    AddTabToTabbedPane.mat(path,jTree2.getLeadSelectionPath().getLastPathComponent().toString());

                    CustomTabbedPane.CloseButton.setIcon(new ImageIcon(Class.class.getResource("/res/exitbutton.png")));
                }

            }
        }
    }//GEN-LAST:event_jTree2MousePressed

